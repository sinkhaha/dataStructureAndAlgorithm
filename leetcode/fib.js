/**
 * 斐波那契数列的4种解法和对比
 */
/**
 * 解法一：暴力递归(自顶向下)
 * 
 * 时间复杂度：O(2^n)，指数级别
 * 空间复杂度：O(n)
 * 
 * 注意点：
 * (1)注意爆栈
 * (2)存在重复的调用计算
 * 
 * @param {*} n 
 */
function fib(n) {
    if (n === 0) {
        return 0;
    }
    if (n === 1 || n === 2) {
        return 1;
    }
    return fib(n-2) + fib(n-1);
}
